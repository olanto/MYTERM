package olanto.myTerm.client.ObjectWrappers;

/**
 *
 * @author simple
 */
public class BooleanWrap {

    private boolean val;

    public BooleanWrap(){
        val = Boolean.FALSE;
    }
    public boolean getVal() {
        return val;
    }

    public void setVal(boolean value) {
        val = value;
    }
}
